const without = (source, itemsToRemove) => {
  let result = [];
  for (let i = 0; i < source.length; i++) {
    if (!itemsToRemove.includes(source[i])) {
      result.push(source[i])
    }
  }
  return result;
}

//test
assertEqual(eqArrays(without([1, 2, 3], [1])));
